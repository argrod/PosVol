	# Echolocation volume calculation
	
	Movement data from the tags are speed, pitch, roll, and heading. The inter-click interval is also available. From this, the volume of 'searchable range' can be calculated.
	"""
end

# ╔═╡ 581f09f4-cc21-48d2-9d58-d67dc22a915d
begin
	# read in the data
	Dat = CSV.File("D++.txt",delim = '\t') |> DataFrame;
	rename!(Dat,[:Depth,:Pitch,:Roll,:Speed,:Heading]);
	#Dat = Dat[1:(sum(ismissing.(Dat.Depth) .== false) - 1),:]
end

# ╔═╡ 77db5a78-6cd3-46fc-ba31-858a5bd2b60c
hcat(sum(ismissing.(Dat.Depth) .== false),sum(ismissing.(Dat.Pitch) .== false),sum(ismissing.(Dat.Roll) .== false),sum(ismissing.(Dat.Speed) .== false),sum(ismissing.(Dat.Heading) .== false))

# ╔═╡ 03f9c08e-b31c-4d24-b45e-ddf400294221
begin
	new = Dat[:,2:3]
	new = new[1:16:nrow(Dat),:]
	new = new[1:sum(ismissing.(Dat.Heading) .== false),:]
	dat = DataFrame(Depth = Dat.Depth[1:sum(ismissing.(Dat.Heading) .== false)], Pitch = new[:,1], Roll = new[:,2], Speed = Dat.Speed[1:sum(ismissing.(Dat.Heading) .== false)], Head = Dat.Heading[sum(ismissing.(Dat.Heading) .== false)])
end

# ╔═╡ 92032536-4310-4d11-8923-7c2947296c79
begin
	pygui(true)
	fig = [];
for b in 1:nrow(dat)
		fig[b] = figure()
		ax = fig[:gca](projection="3d")
		ax[:quiver](0,0,0, dat.Pitch[b],dat.Roll[b],dat.Head[b])
		xlim([minimum(dat.Pitch) - 5, maximum(dat.Pitch) + 5])
		ylim([minimum(dat.Roll) - 5, maximum(dat.Roll) + 5])
		zlim([minimum(dat.Head) - 5, maximum(dat.Head) + 5])
	end
end

